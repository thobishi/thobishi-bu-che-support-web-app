	<br>
	To apply for a login name to access the HEQC-online accreditation system please follow the steps:
	<ul>
		<li>Download and complete the <span class="special">HEQC Online User - Institutional Application form</span></li>
		<li>Click on "Next" in the column on the right of this page	to continue your user registration process</li>
		<li>You will be required to fill in some personal details, upload the completed 
	<span class="special">HEQC Online User - Institutional Application form</span> and submit your application to the CHE.</li>
	</ul>
	Click on this link to download the <a href="<?php echo WRK_DOCUMENTS . "/HEQCOnlineUserRegistrationForm.doc"?>">HEQC Online User - Institutional Application form</a>.
	<br>
	<span class="specialrb"><i>(Note: Save this file to your computer and note the location that you saved it to. Open it, fill in the required information and save it. 
	You will be prompted to upload it when you click Next on this page under actions.)</i></span>
	<br>
	<br>
	The Council on Higher Education (CHE) will consider your application and if your application is approved, 
	you will be the HEQC-online administrator for your institution.  The CHE will then issue you with login details 
	enabling you to access the HEQC-online accreditation system. 
	<br>
	<br>
	If you are an institutional user and you need to gain access to the system, please talk to the HEQC-online administrator 
	for your institution.
	<br>
	<br>
	If you are a public provider, you have already been registered by the Higher Education Quality Committee (HEQC). 
	<br>
	<br>
	<span class="visi">Should you have a problem, please contact the HEQC at 012 349 3934</span>.
