	<b>Welcome to the Higher Education Quality Committee (HEQC) Online Accreditation System.</b>
	<br><br>
	Accreditation is the recognition status given by the HEQC to a programme after an evaluation indicates that it meets the minimum standards for educational quality.   All higher education programmes (NQF levels 5 - 10 on the HEQF) must be submitted to the HEQC for accreditation.
	<br><br>
	The HEQC defines a new programme as one that:
	<ul>
		<li>has not existed before. </li>
		<li>has been substantially changed, i.e. the purpose, the outcomes, the field of study, have been changed to such an extent that it results on a change of 50% or more of their credits. </li>
		<li>has changed mode of delivery.</li>
		<li>is offered at a new site of delivery.</li>
	</ul>
	The accreditation process is based on the assessment of the programme by subject and field specialists based on the criteria in the <img src="images/acrobat.gif"><a href='<?php echo $this->getValueFromTable("settings", "s_key", "offline_form_criteria", "s_value");?>' target="_blank">Criteria for Programme Accreditation (CHE, November, 2004)</a> <i>(Requires Adobe Reader)</i>. The application form that you are about to complete is structured in three sections:
	<br><br>
	Section A is focused on institutional information.
	<br><br>
	Section B is focused on the programme itself and is structured taking into account the minimum criteria that programmes are required to meet.
	<br><br>
	Section C is for programmes offered through distance education.
	<br><br>
	A programme submitted for accreditation may be accredited with or without conditions, or it may not be accredited if it fails to meet minimum standards.  A programme that is not accredited may not be offered.
	<br><br>
	<span class="visi">In the interests of expediting the processing of the application, you are asked to ensure that all questions are answered in full and that all relevant documentation is uploaded as requested.  Failure to provide responses and/ or the relevant documentation will result in the application being returned or cancelled.</span>
