<tr><td colspan="2">
	<b>
Prior to completing an application for accreditation, the institution must update its institutional profile on HEQC-Online.
<ul><li>All contact details in the institutional profile must be kept up to date as the CHE uses this information for correspondence purposes. </li>
<li>All policies must be the latest policies used by the institution. Any document older than five (5) years, and/or not presented as a final document, will not be considered.</li>
<li>The sites of delivery for the institution must be captured in the institutional profile prior to applying to offer a programme / qualification at that site of delivery.</li>
</ul>


The Institutional Profile is accessible from the menu option <a style="color:#0000CD;" onclick="javascript:moveto('_doShowProfile');" target="_blank">Tools / Institutional Profile</a>
<br>
Note: Once you exit this page to go to the Institutional Profile you will need to start the application again. 
