	THE RE-ACCREDITATION APPLICATION FORM
	</span>
</td></tr>
<tr>
	<td>
	<br>
	<p>This re-accreditation application form aims to enable institutions to demonstrate that they meet all of the accreditation criteria. It is designed in such a way that new information submitted may augment, and be read in the light of, information submitted by the institution during the process of accreditation. Particular emphasis is placed on developments within the institution, in terms of policy, procedures and practice, since the submission of the application for accreditation. Thus, for example, policy and procedural documentation needs to be submitted only if it has been drafted and approved, or significantly amended, since the date of submission of the application for accreditation.</p>
	<p>In the initial programme accreditation process, the emphasis was on the extent to which the proposed programme would satisfy the minimum standards pertaining to Input Criteria (programme design; student recruitment, admission and selection; staffing; teaching and learning strategy; student assessment policies and procedures; infrastructure and library resources; programme administrative services; postgraduate policies, regulations and procedures).</p>
	While these Input Criteria form part of the present re-accreditation excercise, particular emphasis will be placed on:
	<ul>
		<li>Process Criteria (programme coordination; academic development for student success; teaching and learning interactions; student assessment practices; coordination of work-based learning; delivery of postgraduate programmes).</li>
		<li>Output and Impact Criteria (student retention and throughput rates; programme impact).</li>
		<li>Review (all of the above programme areas)</li>
	</ul>
	The form provides the format in which applications for re-accreditation should be submitted. It has two sections:
	<br><br>
	Section 1: Institutional information which is the same as the Institutional profile and needs to be updated.
	<br><br>
	Section 2: Programme information.  This must be completed per programme. 
	<br><br>
	
	<span class="visi">Institutions should note that judgements in the evaluation of re-accreditation applications will be based on the evidence provided. 
	Institutions are asked to ensure that the narrative provided is evidence-based and that the relevant supporting documentation is included.</span>
