1.	The application form allows for a single application process that requires all the information necessary for the accreditation of a programme by the Council on Higher Education (CHE) and the registration of the qualification by the South African Qualifications Authority (SAQA) on the National Qualifications Framework (NQF). 
<br><br>
2.	Incomplete or plagiarised applications will be returned, and the applicable application fee will not be refunded. Plagiarised content includes information copied from the internet, SAQA website (copying a registered qualification), or another institution’s information. Policy documents must be on the institutional letterhead and aligned with the vision and mission of the institution. 
<br><br>
3.	The institution is not permitted to offer the accredited programme / registered qualification of another institution. The institution is not permitted to outsource delivery of its accredited programme / registered qualification. Note that institutions operating as separate legal entities under a holding company are regarded as separate institutions. The institution (i.e. “juristic person”; HE Act, 1997) assumes full responsibility for the programme / qualification. Submitting an application across different brands of a holding company / affiliated brands that presents high similarity in programme / qualification design will be regarded as plagiarism.  
<br><br>
4.	Information found to be misrepresented will result in the application being returned and the applicable fee will not be refunded.
<br><br>
5.	The programme / qualification may not be marketed or offered prior to accreditation by the CHE, PQM approval or registration by the Department of Higher Education and Training, and registration by SAQA on the NQF.  Institutions found to be advertising a programme / qualification on their websites prior to such registration will be liable to having the programme accreditation / qualification registration withdrawn. 
<br><br>
6.	The conceptualisation, design, delivery, and assessment of the programme / qualification (including the development of study / learning material) is the responsibility of, and must be undertaken by, the institution and the academics in its employ.  The CHE recognises that relevant stakeholders, including academic peers from outside the institution, and employers and professional bodies where applicable, may be involved in the development of the programme / qualification. However, the aforementioned activities must not be outsourced to consultants or other third parties as the CHE will verify institutional capacity for curriculum development and programme delivery.  
<br><br>
7.	The institution must be acquainted with the relevant policy documents of the DHET, CHE, SAQA and relevant statutory professional body (if applicable) when applying for programme accreditation and registration of the qualification. The information provided must comply with the <i>Higher Education Qualifications Sub-Framework, Criteria for Programme Accreditation and Policy and Criteria for the Registration of Qualifications and Part-qualifications on the National Qualifications Framework</i>. In addition, there must be compliance with the <i>Level Descriptors for the South African National Qualifications Framework</i>, and relevant national legislation and policy. 
<br><br>
8.	Following approval by the relevant institutional structures, the application must be submitted by the Institutional Administrator or Academic Head / Deputy Vice-Chancellor (Academic / Teaching and Learning), or Chief Executive Officer (for private institutions) or any suitable, designated person, who is a permanent employee of the institution. 
<br><br>
9.	If the programme / qualification is not accredited, the institution will receive ONE opportunity for a representation in this regard (Note that the representation must be submitted within 21 working days. The application will be closed if the due date is not met). If the programme / qualification is not accredited following the representation, the application will be closed, and the institution will have to submit a new application. The decision of the Higher Education Quality Committee will be final. The institution may only submit a new application after one year of receipt of the Higher Education Quality Committee’s decision on the representation.
<br><br>
10.	All institutional policies and procedures relating to this application must be up to date and aligned to the CHE and national higher education policies and guidelines and must be approved and signed by the authorised person(s) at the institution, dated, and uploaded on the institutional profile. 
<br><br>
11.  The institution consents, in line with the Protection of Personal Information Act (POPIA), that personal information provided in the application and Institutional Profile may be used by parties involved in the processing of the accreditation application and the registration of the qualification on the National Qualifications framework and the Department of Higher Education Registers. 

