	<b>Welcome to the Higher Education Quality Committee (HEQC) Accreditation System.</b>
	<br><br>
	The HEQC has initiated its first programme accreditation cycle 2004-2006. During this cycle only new programmes will be accredited. This is to say, 
	<br><br>
	<ul>
		<li>
		programmes that have not existed before. 
		</li>
		<li>
		programmes that have been substantially changed, i.e. the purpose, the outcomes, the field of study, have been changed to such an extent that it results on a change of 50% or more of their credits. 
		</li>
		<li>
		programmes that have changed their mode of delivery.
		</li>
	</ul>
	Accreditation is the recognition status given for a stipulated period of time given by the HEQC to a programme after 
	an evaluation indicates that it meets or exceeds a minimum threshold of educational quality. 
