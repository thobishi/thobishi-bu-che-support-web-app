	</p>
	The re-accreditation submission focuses on the following areas aligned to the Criteria for Programme Accreditation (2004):
	<ul>
		<li>Conditions from the previous re-accreditation cycle</li>
		<li>Programme design</li>
		<li>Teaching and learning interactions</li>
		<li>Assessment practices</li>
		<li>Academic development including student support</li>
		<li>Infrastructure</li>
		<li>Student and retention throughput rates</li>
		<li>Programme coordination and programme review</li>
	</ul>
	
	<p>The template is structured into eight sections corresponding to the eight areas. The first section focuses on conditions/recommendations set by the HEQC to improve the quality of programmes from previous applications for re-accreditation. In this section the institution is to outline the quality improvements affected that address the issues raised by the HEQC previously and provide details of the institutional plans to address outstanding recommendations that have not yet been addressed. </p>
	The remaining seven sections are divided into two parts. The first part contains statements related to the focus area. The institution is to interrogate each statement, consider all relevant data available to it and in relation to each statement rate whether its programme: 
	<ul>
		<li>exceeds minimum standards.</li>
		<li>meets minimum standards.</li>
		<li>partially meets minimum standards, but with improvement needed.</li>
		<li>partially meets minimum standards, but with significant improvement needed.</li>
		<li>does not meet the majority of the minimum standards.</li>
		<li>Review (all of the above programme areas)</li>
	</ul>
	It is then to justify its rating in a concise, analytical and reflective summary in relation to minimum requirements. Minimum requirements should include the Criteria for Programme Accreditation (2004), the Higher Education Qualifications Sub-Framework (2013), Policies on the Recognition of Prior Learning, Credit Accumulation and Transfer, and Assessment in higher education (2016), Level Descriptors for the South African National Qualifications Framework (2012) and good higher education practice. If the programme needs improvement in any area, the summary should clearly indicate what actionable improvements will be taken by the institution within specified timeframes to ensure that the programme meets minimum requirements.
	<br>
	The second part  of each section requires the institution to respond to each statement by reflecting on all of its responses in its submission and then provide a concise narrative that demonstrates an integrated and robust analysis on the management of the quality of its programmes at a programme level and where useful an institutional level. 
	<br><br>
	The CHE requests authentic responses to each section.
