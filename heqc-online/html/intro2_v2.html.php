<br>
	<b>The accreditation process of a new programme goes through three stages:</b>
	<br><br>
	<ul>
		<li>
		Accreditation: during this phase institutions submit evidence of their capacity and institutional support to start offering a
		programme. During this phase the HEQC will scrutinise the arrangements for the quality assurance of teaching and learning
		at the institutions. If a programme satisfies the HEQC's requirements for the accreditation phase it will receive provisional
		accreditation. Read more on accreditation phase (link to accreditation page)
		<br><br>
		</li>
		<li>
		Midterm-check: constitutes a performance and compliance check midway through the programme to ensure that the institution has
		implemented the stated programme plan and has identified and addressed areas for urgent attention.
		<br><br>
		</li>
		<li>
		Accreditation: Within a year of the graduation of the first cohort of students of the new programme the institution must
		demonstrate compliance with conditions set by the HEQC during the previous phases.
		</li>
	</ul>
	<br><br>
