<tr><td colspan="2">
Prior to completing an application for accreditation, the institution must update their institutional profile.
<ul><li>All contact details in the Institutional Profile must be up to date because the CHE uses this contact information for correspondence.</li>
<li>All policies must be the latest policies used by the institution.</li>
<li>The sites of delivery for the institution must be captured in the Institutional Profile prior to applying to offer a qualification at that site of delivery.</li>
</ul>


The Institutional Profile is accessible from the menu option Tools / Institutional Profile.

